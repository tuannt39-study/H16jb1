    private void jbtchiaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtchiaActionPerformed
        String number1 = jtfx.getText();
        String number2 = jtfy.getText();
        double num1 = 0;
        double num2 = 0;

        try {
            num1 = Integer.parseInt(jtfx.getText().toString());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Nhập sai kiểu");
            jtfx.requestFocus();
            return;
        }
        try {
            num2 = Integer.parseInt(jtfy.getText().toString());
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Nhập sai kiểu");
            jtfy.requestFocus();
            return;
        }
        
        if (num2 == 0) {
                jlbketqua.setForeground(Color.red);
                jlbketqua.setText("không chia được");
                JOptionPane.showMessageDialog(null, "không chia được cho 0");
                jtfy.requestFocus();
            } else {
                double sum = 0;
                
                sum = num1 / num2;
                jlbketqua.setForeground(Color.blue);
                jlbketqua.setText(new Double(sum).toString());
            }
    }//GEN-LAST:event_jbtchiaActionPerformed

