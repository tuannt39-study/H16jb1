    private void jbtcongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtcongActionPerformed
        String number1 = jtfx.getText();
        String number2 = jtfy.getText();
        double num1 = 0;
        double num2 = 0;
        double temp = 0;
        try {
            num1 = Integer.parseInt(jtfx.getText().toString());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Nhập sai kiểu");
            jtfx.requestFocus();
            return;
        }
        try {
            num2 = Integer.parseInt(jtfy.getText().toString());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Nhập sai kiểu");
            jtfx.requestFocus();
            return;
        }

        temp = num1 + num2;
        jlbketqua.setForeground(Color.blue);
        jlbketqua.setText(new Double(temp).toString());
    }//GEN-LAST:event_jbtcongActionPerformed

